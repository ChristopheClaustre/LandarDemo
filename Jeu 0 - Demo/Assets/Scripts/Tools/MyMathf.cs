public class MyMathf
{
    #region Methods
    /***************************************************/
    /***  METHODS               ************************/
    /***************************************************/

    /********  PUBLIC           ************************/

    public static float PosModulo(float f, float m)
    {
        f = f % m;
        if (f < 0)
            f += m;
        return f;
    }

    public static int PosModulo(int i, int m)
    {
        i = i % m;
        if (i < 0)
            i += m;
        return i;
    }

    /********  PROTECTED        ************************/

    /********  PRIVATE          ************************/

    #endregion
}
